package gabmler.tools.cli;

public class SystemInitException extends Exception {

	private static final long serialVersionUID = 887233589674209688L;

	public SystemInitException() {
		super();
	}

	public SystemInitException(String message, Throwable cause) {
		super(message, cause);
	}

	public SystemInitException(String message) {
		super(message);
	}

	public SystemInitException(Throwable cause) {
		super(cause);
	}

}
