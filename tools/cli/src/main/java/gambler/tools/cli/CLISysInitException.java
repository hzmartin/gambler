package gambler.tools.cli;

public class CLISysInitException extends Exception {

	private static final long serialVersionUID = 887233589674209688L;

	public CLISysInitException() {
		super();
	}

	public CLISysInitException(String message, Throwable cause) {
		super(message, cause);
	}

	public CLISysInitException(String message) {
		super(message);
	}

	public CLISysInitException(Throwable cause) {
		super(cause);
	}

}
