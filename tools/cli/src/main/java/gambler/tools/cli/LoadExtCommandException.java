package gambler.tools.cli;

public class LoadExtCommandException extends Exception {

	private static final long serialVersionUID = 887233589674209688L;

	public LoadExtCommandException() {
		super();
	}

	public LoadExtCommandException(String message, Throwable cause) {
		super(message, cause);
	}

	public LoadExtCommandException(String message) {
		super(message);
	}

	public LoadExtCommandException(Throwable cause) {
		super(cause);
	}

}
